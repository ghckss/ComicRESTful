package com.triangle.domain;

public class EpisodeVO {

	private int indexNumber;
	private String webtoonId;
	private String episode;
	private String episodeThumbnail;
	private String episodeLink;
	private String episodeDate;
	private String charge;
	private String myEpisodeThumbnail;
	
	public int getIndexNumber() {
		return indexNumber;
	}
	public void setIndexNumber(int indexNumber) {
		this.indexNumber = indexNumber;
	}
	public String getWebtoonId() {
		return webtoonId;
	}
	public void setWebtoonId(String webtoonId) {
		this.webtoonId = webtoonId;
	}
	public String getEpisode() {
		return episode;
	}
	public void setEpisode(String episode) {
		this.episode = episode;
	}
	public String getEpisodeThumbnail() {
		return episodeThumbnail;
	}
	public void setEpisodeThumbnail(String episodeThumbnail) {
		this.episodeThumbnail = episodeThumbnail;
	}
	public String getEpisodeLink() {
		return episodeLink;
	}
	public void setEpisodeLink(String episodeLink) {
		this.episodeLink = episodeLink;
	}
	public String getEpisodeDate() {
		return episodeDate;
	}
	public void setEpisodeDate(String episodeDate) {
		this.episodeDate = episodeDate;
	}
	public String getCharge() {
		return charge;
	}
	public void setCharge(String charge) {
		this.charge = charge;
	}
	public String getMyEpisodeThumbnail() {
		return myEpisodeThumbnail;
	}
	public void setMyEpisodeThumbnail(String myEpisodeThumbnail) {
		this.myEpisodeThumbnail = myEpisodeThumbnail;
	}
}
